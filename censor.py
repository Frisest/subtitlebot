# Нормализуем ключи: нижний регистр, ё→е для вариантов без ё
def _build_lookup(censor_map: dict) -> dict:
    lookup = {}
    for word, replacement in censor_map.items():
        key = word.lower()
        lookup[key] = replacement
        # Также добавляем вариант с е вместо ё
        key_no_yo = key.replace("ё", "е")
        if key_no_yo != key:
            lookup[key_no_yo] = replacement
    return lookup

_LOOKUP = _build_lookup(CENSOR_MAP)


def censor_text(text: str) -> str:
    """
    Заменяет матные слова в тексте на цензурные версии.
    Сохраняет регистр первой буквы.
    """
    # Разбиваем на токены (слова + не-слова), чтобы сохранить пунктуацию и пробелы
    tokens = re.split(r'(\W+)', text)
    result = []
    for token in tokens:
        if not token:
            continue
        key = token.lower().replace("ё", "е")
        if key in _LOOKUP:
            replacement = _LOOKUP[key]
            # Сохраняем заглавную букву если оригинал с заглавной
            if token[0].isupper():
                replacement = replacement[0].upper() + replacement[1:]
            result.append(replacement)
        else:
            result.append(token)
    return "".join(result)


def censor_segments(segments: list[dict]) -> list[dict]:
    """Применяет цензуру ко всем субтитрам."""
    return [
        {**seg, "text": censor_text(seg["text"])}
        for seg in segments
    ]
